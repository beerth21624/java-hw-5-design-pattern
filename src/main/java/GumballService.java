import java.util.Random;

interface State {
    void insertQuarter();
    void ejectQuarter();
    void turnCrank();
    void dispense();
    void choose(String flavor);
}

class GumballMachine {
    State noQuarterState;
    State hasQuarterState;
    State soldState;
    State soldOutState;
    State winnerState;
    State flavorChoiceState;

    State state;
    int count = 0;
    String currentFlavor = null;

    public GumballMachine(int numberGumballs) {
        noQuarterState = new NoQuarterState(this);
        hasQuarterState = new HasQuarterState(this);
        soldState = new SoldState(this);
        soldOutState = new SoldOutState(this);
        winnerState = new WinnerState(this);
        flavorChoiceState = new FlavorChoiceState(this);

        this.count = numberGumballs;
        if (numberGumballs > 0) {
            state = noQuarterState;
        } else {
            state = soldOutState;
        }
    }

    public void insertQuarter() {
        state.insertQuarter();
    }

    public void ejectQuarter() {
        state.ejectQuarter();
    }

    public void turnCrank() {
        state.turnCrank();
        state.dispense();
    }

    public void choose(String flavor) {
        state.choose(flavor);
    }

    void setState(State state) {
        this.state = state;
    }

    void releaseBall() {
        System.out.println("A " + currentFlavor + " gumball comes rolling out the slot...");
        if (count > 0) {
            count = count - 1;
        }
    }

    int getCount() {
        return count;
    }

    void refill(int count) {
        this.count += count;
        System.out.println("The gumball machine was just refilled; its new count is: " + this.count);
        state = noQuarterState;
    }

    public State getState() {
        return state;
    }

    public State getNoQuarterState() {
        return noQuarterState;
    }

    public State getHasQuarterState() {
        return hasQuarterState;
    }

    public State getSoldState() {
        return soldState;
    }

    public State getSoldOutState() {
        return soldOutState;
    }

    public State getWinnerState() {
        return winnerState;
    }

    public State getFlavorChoiceState() {
        return flavorChoiceState;
    }

    public String getCurrentFlavor() {
        return currentFlavor;
    }

    public void setCurrentFlavor(String flavor) {
        this.currentFlavor = flavor;
    }
}

